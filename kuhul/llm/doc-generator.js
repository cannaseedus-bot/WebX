/**
 * @fileoverview LLM-based documentation generator for KUHUL source.
 *
 * Analyses a KUHUL program and generates human-readable documentation
 * describing what each block does.
 *
 * @module kuhul/llm/doc-generator
 */

import { KuhulLexer }   from '../compiler/lexer.js';
import { KuhulParser }  from '../compiler/parser.js';
import { NodeKind }     from '../compiler/parser.js';

// ------------------------------------------------------------------ //
// Glyph descriptions
// ------------------------------------------------------------------ //

const GLYPH_DESC = {
  '⊗': 'tensor product (matrix multiplication)',
  '⊕': 'element-wise addition',
  '⊖': 'element-wise subtraction',
  '⊛': 'convolution',
  '⊜': 'equality / assignment',
  '⊝': 'negation',
  '⊞': 'direct sum (concatenation)',
};

// ------------------------------------------------------------------ //
// DocGenerator
// ------------------------------------------------------------------ //

/** Generates documentation from KUHUL source code. */
export class DocGenerator {
  /**
   * Generate Markdown documentation for the given KUHUL source.
   *
   * @param {string} source - KUHUL source text
   * @returns {Promise<string>} Markdown documentation
   */
  async generateDocs(source) {
    let ast;
    try {
      const tokens = new KuhulLexer().lex(source);
      ast = new KuhulParser().parse(tokens);
    } catch (err) {
      return `# Documentation Error\n\nCould not parse source: ${err.message}\n`;
    }

    const lines = [];
    lines.push('# KUHUL Program Documentation');
    lines.push('');
    lines.push('> Auto-generated by KUHUL DocGenerator');
    lines.push('');

    this._docBody(ast.body, lines, 0);
    return lines.join('\n');
  }

  // ---------------------------------------------------------------- //
  // AST walkers
  // ---------------------------------------------------------------- //

  /**
   * @param {object[]} body
   * @param {string[]} lines
   * @param {number}   depth
   */
  _docBody(body, lines, depth) {
    for (const stmt of body) {
      this._docStatement(stmt, lines, depth);
    }
  }

  _docStatement(stmt, lines, depth) {
    const h = '#'.repeat(Math.min(depth + 2, 6));
    switch (stmt.kind) {
      case NodeKind.Fold:
        lines.push(`${h} Fold Block`);
        lines.push('');
        lines.push('A scoped block of KUHUL statements.');
        lines.push('');
        this._docBody(stmt.body, lines, depth + 1);
        break;

      case NodeKind.Allocation: {
        const { name }              = stmt.identifier;
        const { elementType, shape } = stmt.tensorType;
        lines.push(`**Allocate** \`${name}\`: \`tensor<${elementType}, [${shape.join(', ')}]>\``);
        lines.push('');
        break;
      }

      case NodeKind.Read:
        lines.push(`**Read** identifier \`${stmt.identifier.name}\` into the execution context.`);
        lines.push('');
        break;

      case NodeKind.Write:
        lines.push(`**Write** value \`${this._valStr(stmt.value)}\` to \`${stmt.identifier.name}\`.`);
        lines.push('');
        break;

      case NodeKind.Operation: {
        const desc = GLYPH_DESC[stmt.glyph] ?? `unknown glyph ${stmt.glyph}`;
        const ops  = stmt.operands.map(o => `\`${this._valStr(o)}\``).join(', ');
        lines.push(`**Operation** \`${stmt.glyph}\` (${desc}) on ${ops}.`);
        lines.push('');
        break;
      }

      case NodeKind.PhaseCycle:
        lines.push(`${h} Phase Cycle`);
        lines.push('');
        lines.push('Executes the enclosed body repeatedly through a full phase cycle (0 → 2π).');
        lines.push('');
        this._docBody(stmt.body, lines, depth + 1);
        break;

      case NodeKind.Invocation: {
        const args = stmt.args.map(a => `\`${this._valStr(a)}\``).join(', ');
        lines.push(`**Invoke** function \`${stmt.identifier.name}\`${args ? ` with ${args}` : ''}.`);
        lines.push('');
        break;
      }

      default:
        lines.push(`*Unknown statement kind: ${stmt.kind}*`);
        lines.push('');
    }
  }

  _valStr(node) {
    if (!node) return '';
    if (node.kind === NodeKind.Identifier) return node.name;
    if (node.kind === NodeKind.NumberLit)  return String(node.value);
    if (node.kind === NodeKind.StringLit)  return `"${node.value}"`;
    return '?';
  }
}
